const dictionaries: Record<Exclude<UiLocale, "en">, Record<string, string>> = { zh, ja };

export function resolveUiLocale(setting: string | null | undefined, systemLanguage = navigator.language): UiLocale {
  const candidate = (setting && setting !== "auto" ? setting : systemLanguage).toLowerCase();
  if (candidate === "zh" || candidate.startsWith("zh-")) return "zh";
  if (candidate === "ja" || candidate.startsWith("ja-")) return "ja";
  if (candidate === "en" || candidate.startsWith("en-")) return "en";
  return "en";
}

export function translate(locale: UiLocale, key: string, parameters: Parameters = {}): string {
  const template = locale === "en" ? key : dictionaries[locale][key] ?? key;
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, name: string) =>
    Object.hasOwn(parameters, name) ? String(parameters[name]) : match);
}
