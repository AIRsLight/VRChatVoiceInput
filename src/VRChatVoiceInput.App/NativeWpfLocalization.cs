    public static string Translate(string? languageSetting, string key)
    {
        var locale = NativeLocalization.Resolve(languageSetting);
        var translations = locale == "zh" ? Chinese : locale == "ja" ? Japanese : null;
        return translations is not null && translations.TryGetValue(key, out var value) ? value : key;
    }
